class _ButtonCollector(HTMLParser):
    """Collect button tags and their attributes."""
    def __init__(self):
        super().__init__()
        self.buttons = []          # list of attr dicts
        self.spans   = []          # list of (id, attr dict)

    def handle_starttag(self, tag, attrs):
        d = dict(attrs)
        if tag == "button":
            self.buttons.append(d)
        elif tag == "span":
            self.spans.append(d)


class TestAudioPlayerAccessibility(unittest.TestCase):

    @classmethod
    def setUpClass(cls):
        with open(_AUDIO_HTML, encoding="utf-8") as f:
            cls.html = f.read()
        p = _ButtonCollector()
        p.feed(cls.html)
        cls.buttons = p.buttons
        cls.spans   = p.spans

    # -- Buttons ---------------------------------------------------------------

    def test_all_buttons_have_aria_label(self):
        """Every <button> must have a non-empty aria-label."""
        for btn in self.buttons:
            self.assertIn(
                "aria-label", btn,
                f"Button missing aria-label: {btn}",
            )
            self.assertTrue(btn["aria-label"].strip(), f"Empty aria-label on {btn}")

    def test_back_button_aria_label(self):
        back = next((b for b in self.buttons if b.get("id") == "btn-back"), None)
        self.assertIsNotNone(back, "btn-back not found")
        self.assertIn("1", back["aria-label"].lower())

    def test_forward_button_aria_label(self):
        fwd = next((b for b in self.buttons if b.get("id") == "btn-fwd"), None)
        self.assertIsNotNone(fwd, "btn-fwd not found")
        self.assertIn("1", fwd["aria-label"].lower())

    def test_stop_button_aria_label(self):
        stop = next((b for b in self.buttons if b.get("id") == "btn-stop"), None)
        self.assertIsNotNone(stop)
        self.assertEqual(stop["aria-label"].lower(), "stop")

    def test_pin_button_aria_label(self):
        pin = next((b for b in self.buttons if b.get("id") == "btn-pin"), None)
        self.assertIsNotNone(pin)
        self.assertIn("split", pin["aria-label"].lower())

    def test_play_button_initial_aria_label_is_play(self):
        play = next((b for b in self.buttons if b.get("id") == "btn-play"), None)
        self.assertIsNotNone(play)
        self.assertEqual(play["aria-label"].lower(), "play")

    # -- Time display span -----------------------------------------------------

    def test_time_display_has_role_timer(self):
        td = next((s for s in self.spans if s.get("id") == "time-display"), None)
        self.assertIsNotNone(td, "time-display span not found")
        self.assertEqual(td.get("role"), "timer")

    def test_time_display_has_aria_live(self):
        td = next((s for s in self.spans if s.get("id") == "time-display"), None)
        self.assertIsNotNone(td)
        self.assertIn("aria-live", td)

    # -- Pin label (live region) -----------------------------------------------

    def test_pin_label_has_aria_live_polite(self):
        pl = next((s for s in self.spans if s.get("id") == "pin-label"), None)
        self.assertIsNotNone(pl, "pin-label span not found")
        self.assertEqual(pl.get("aria-live"), "polite")

    # -- Play/pause aria-label updated in JS -----------------------------------

    def test_js_updates_aria_label_on_play(self):
        """JS must call setAttribute('aria-label', 'Pause') when starting playback."""
        self.assertIn(
            "setAttribute(\"aria-label\", \"Pause\")", self.html,
            "JS does not update aria-label to 'Pause' on play",
        )

    def test_js_updates_aria_label_on_pause(self):
        self.assertIn(
            "setAttribute(\"aria-label\", \"Play\")", self.html,
            "JS does not update aria-label back to 'Play' on pause/stop",
        )

    def test_js_aria_label_updated_on_stop(self):
        """Stop handler and end-of-phrase handler must both reset aria-label."""
        count = self.html.count("setAttribute(\"aria-label\", \"Play\")")
        self.assertGreaterEqual(count, 3, "Expected ≥3 aria-label='Play' resets (stop, end-boundary, onRender)")


