# C3 — BPM bar-text logic
# ===========================================================================

def _bar_texts(phrases, bar_widths):
    """Mirror of the bar_texts computation in assessment._render_phrases_timeline."""
    total_dur = sum(bar_widths) or 1
    return [
        f"{ph.get('bpm', 0):.0f}" if (w / total_dur) > 0.04 else ""
        for ph, w in zip(phrases, bar_widths)
    ]


class TestBpmBarTextLogic(unittest.TestCase):

    def _ph(self, bpm):
        return {"bpm": bpm}

    def test_wide_bar_shows_label(self):
        """A bar wider than 4% of total should show a BPM number."""
        phrases    = [self._ph(120)]
        widths     = [1000]
        labels     = _bar_texts(phrases, widths)
        self.assertEqual(labels[0], "120")

    def test_narrow_bar_suppressed(self):
        """A bar ≤ 4% of total width should have an empty label."""
        # 1 narrow bar + 1 very wide bar; narrow is < 4%
        phrases = [self._ph(80), self._ph(120)]
        widths  = [30, 970]          # 30/1000 = 3% → suppressed
        labels  = _bar_texts(phrases, widths)
        self.assertEqual(labels[0], "", "Narrow bar should not have a label")
        self.assertEqual(labels[1], "120")

    def test_exactly_4pct_shows_label(self):
        """A bar at exactly 4.01% should show a label."""
        phrases = [self._ph(95), self._ph(95)]
        widths  = [401, 9599]        # 401/10000 = 4.01%
        labels  = _bar_texts(phrases, widths)
        self.assertEqual(labels[0], "95")

    def test_exactly_at_threshold_suppressed(self):
        """A bar at exactly 4% (not >4%) should be suppressed."""
        phrases = [self._ph(95), self._ph(95)]
        widths  = [400, 9600]        # 400/10000 = 4.00% → not > 0.04
        labels  = _bar_texts(phrases, widths)
        self.assertEqual(labels[0], "")

    def test_label_format_integer(self):
        """BPM label must be a rounded integer string (no decimal)."""
        phrases = [self._ph(123.6)]
        widths  = [1000]
        labels  = _bar_texts(phrases, widths)
        self.assertNotIn(".", labels[0])
        self.assertEqual(labels[0], "124")

    def test_all_equal_width_bars_all_shown(self):
        """When bars are equal width and there are <= 25, all should be shown."""
        phrases = [self._ph(100 + i) for i in range(5)]
        widths  = [200] * 5          # each = 20% → all > 4%
        labels  = _bar_texts(phrases, widths)
        self.assertTrue(all(lbl != "" for lbl in labels))

    def test_empty_phrases_no_error(self):
        labels = _bar_texts([], [])
        self.assertEqual(labels, [])

    def test_single_bar_always_shown(self):
        """A single bar is 100% of total — must always show label."""
        phrases = [self._ph(85)]
        widths  = [5000]
        labels  = _bar_texts(phrases, widths)
        self.assertEqual(labels[0], "85")


