# ===========================================================================

class TestChartCaptions(unittest.TestCase):
    """Check that each significant Plotly chart in assessment.py is followed
    by an st.caption() call within a short window of source lines."""

    @classmethod
    def setUpClass(cls):
        with open(_ASSESSMENT_PY, encoding="utf-8") as f:
            cls.lines = f.readlines()

    def _caption_follows_chart(self, chart_lineno: int, window: int = 8) -> bool:
        """Return True if st.caption() appears within *window* lines after the chart."""
        for i in range(chart_lineno, min(chart_lineno + window, len(self.lines))):
            if "st.caption(" in self.lines[i]:
                return True
        return False

    def _find_plotly_lines(self):
        return [
            i for i, line in enumerate(self.lines)
            if "st.plotly_chart(" in line
        ]

    def test_phrase_timeline_has_caption(self):
        """_render_phrases_timeline: caption describes colour meaning."""
        src = "".join(self.lines)
        # The timeline caption already existed; verify it's still there.
        self.assertIn(
            "Colour", src,
            "Phrase timeline caption should describe colour meaning",
        )

    def test_bpm_step_chart_has_caption(self):
        """_render_bpm_step_chart: caption describes transitions."""
        src = "".join(self.lines)
        self.assertIn(
            "BPM step chart", src,
            "BPM step chart should have an accessible caption",
        )

    def test_behavioral_tag_chart_has_caption(self):
        """_render_patterns_section bar chart: caption describes content."""
        src = "".join(self.lines)
        self.assertIn(
            "Behavioral tag frequency", src,
            "Behavioral tag chart should have an accessible caption",
        )

    def test_all_plotly_charts_followed_by_caption(self):
        """Every st.plotly_chart() call in assessment.py must be followed
        by an st.caption() within 8 lines."""
        chart_lines = self._find_plotly_lines()
        self.assertGreater(len(chart_lines), 0, "No plotly charts found in assessment.py")

        missing = []
        for lineno in chart_lines:
            if not self._caption_follows_chart(lineno):
                missing.append(lineno + 1)   # 1-based for error messages

        self.assertEqual(
            missing, [],
            f"Charts at lines {missing} in assessment.py have no caption within 8 lines",
        )


