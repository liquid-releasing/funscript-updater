# M1 — No label_visibility="collapsed" remaining in panel source files
# ===========================================================================

class TestNoCollapsedLabels(unittest.TestCase):
    """Assert that label_visibility="collapsed" has been removed from panels."""

    def _panel_files(self):
        return [
            os.path.join(_PANELS_DIR, f)
            for f in os.listdir(_PANELS_DIR)
            if f.endswith(".py")
        ]

    def test_no_collapsed_label_visibility_in_panels(self):
        """No panel should suppress labels from the accessibility tree."""
        violations = []
        for path in self._panel_files():
            with open(path, encoding="utf-8") as f:
                src = f.read()
            matches = list(re.finditer(r'label_visibility\s*=\s*["\']collapsed["\']', src))
            for m in matches:
                lineno = src[:m.start()].count("\n") + 1
                violations.append(f"{os.path.basename(path)}:{lineno}")

        self.assertEqual(
            violations, [],
            f"label_visibility='collapsed' found in:\n  " + "\n  ".join(violations),
        )


