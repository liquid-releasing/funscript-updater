class TestLangInjection(unittest.TestCase):

    @classmethod
    def setUpClass(cls):
        with open(_APP_PY, encoding="utf-8") as f:
            cls.src = f.read()

    def test_lang_injection_present(self):
        """app.py must inject lang='en' onto the document element."""
        self.assertIn(
            "documentElement.lang", self.src,
            "Missing lang='en' JS injection in app.py",
        )

    def test_lang_value_is_en(self):
        match = re.search(r"documentElement\.lang\s*=\s*['\"](\w+)['\"]", self.src)
        self.assertIsNotNone(match, "lang assignment not found")
        self.assertEqual(match.group(1), "en")

    def test_lang_inside_sentinel_block(self):
        """Lang injection must be inside the __forgeKeysRegistered sentinel to avoid
        running on every Streamlit rerun."""
        sentinel_pos = self.src.find("__forgeKeysRegistered")
        lang_pos     = self.src.find("documentElement.lang")
        self.assertGreater(
            lang_pos, sentinel_pos,
            "lang injection should appear after the sentinel flag check",
        )

    def test_sr_only_css_injected_globally(self):
        """.sr-only CSS class must be injected in app.py for all panels to use."""
        self.assertIn(
            ".sr-only", self.src,
            "Global .sr-only CSS not found in app.py",
        )


